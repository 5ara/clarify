        /**
        * Convert a hex value to rgb and hsl values
        *
        * @param $color
        * @return array
        */
        function HtmltoHsl($color) {
            if($color[0] == '#') {
                $color = substr($color, 1);
            }

            if (strlen($color) == 3) {
                $color = $color[0] . $color[0] . $color[1] . $color[1] . $color[2] . $color[2];
            }

            $r = hexdec($color[0] . $color[1])/255;
            $g = hexdec($color[2] . $color[3])/255;
            $b = hexdec($color[4] . $color[5])/255;

            $min = min($r,min($g,$b));
            $max = max($r,max($g,$b));
            $delta = $max - $min;
            $l = ($min + $max) / 2;

            $s = 0;
            if($l > 0 && $l < 1) {
                $temp = $l < 0.5 ? (2 * $l) : (2 - 2 * $l);
                $s = $delta / ($temp);
            }

            $h = 0;
            if($delta > 0) {
                if ($max == $r && $max != $g) {
                    $h += ($g - $b) / $delta;
                }
                if ($max == $g && $max != $b) {
                    $h += (2 + ($b - $r) / $delta);
                }
                if ($max == $b && $max != $r) {
                    $h += (4 + ($r - $g) / $delta);
                }
                $h = $h/6;
            }

            $h = $h < 0 ? 1-$h : $h;
            return array('h' =>  intval($h*360), 's' => intval($s*100),'l' => intval($l*100), 'r'=>$r, 'g'=>$g, 'b'=>$b);
        }

        /**
         * Find name values vor hex colors and identify perfect matches
         *
         * @param $color
         * @return array
         */
        function getColorMatch($color) {

            $colorValues = $this->HtmltoHsl($color);

            $df = -1;
            $cl = -1;

            for($i = 0; $i < count($this->colornames); $i++) {
                if ($color == "#".ucwords($this->colornames[$i][0])) {
                    return array($this->colornames[$i][1],true);
                }
                $ndf1 = pow($colorValues['r'] - $this->colornames[$i][2],2) + pow($colorValues['g'] - $this->colornames[$i][3],2) + pow($colorValues['b'] - $this->colornames[$i][4],2);
                $ndf2 = pow($colorValues['h'] - $this->colornames[$i][5],2) + pow($colorValues['s'] - $this->colornames[$i][6],2) + pow($colorValues['l'] - $this->colornames[$i][7],2);
                $ndf = $ndf1 + $ndf2*2;
                if ($df < 0 || $df > $ndf) {
                    $df = $ndf;
                    $cl = $i;
                }
            }
            return ($cl < 0 ? array("No Match of " + $color, false) : array($this->colornames[$cl][1], false));
        }


